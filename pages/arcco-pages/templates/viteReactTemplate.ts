/** Retorna o template com nome do projeto customizado no package.json */
export function getProjectTemplate(projectName: string): FileSystemTree {
  const template = { ...VITE_REACT_TEMPLATE };
  const pkg = JSON.parse(template['package.json']);
  const safeName = projectName.toLowerCase().replace(/[^a-z0-9-]/g, '-').replace(/-+/g, '-').slice(0, 50);
  pkg.name = safeName || 'arcco-app';
  template['package.json'] = JSON.stringify(pkg, null, 2);
  return template;
}
